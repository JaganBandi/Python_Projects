# Python program using array (list)

# Create an array
numbers = [10, 25, 7, 42, 18]

print("Array Elements:", numbers)

# Print elements one by one
print("\nElements in Array:")
for num in numbers:
    print(num, end=" ")

# Sum of array elements
total = sum(numbers)
print("\n\nSum of Array Elements:", total)

# Maximum element
print("Maximum Element:", max(numbers))

# Minimum element
print("Minimum Element:", min(numbers))
